    private void botaoInsereNaListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoInsereNaListaActionPerformed
        txtListaCompra.setText("");
        int codBarra = Integer.parseInt(txtCodBarra.getText());
        int qtd = Integer.parseInt(txtQtd.getText());
        txtListaCompra.append(refRegistradora.incluirItem(codBarra, qtd));
        txtCodBarra.setText("");
        txtQtd.setText("");
        txtCodBarra.requestFocusInWindow();
        //JOptionPane.showMessageDialog(null, codBarra);
    }//GEN-LAST:event_botaoInsereNaListaActionPerformed

